package myjsp.userPack;

public class User {
	private String id;
	private String passwd;
	private String name;
	private String rrnum;
	private String tel;
	private String mailnum;
	private String address;
	private String email;
	private String domain;
	private String forgetq;
	private String forgeta;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRrnum() {
		return rrnum;
	}
	public void setRrnum(String rrnum) {
		this.rrnum = rrnum;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getMailnum() {
		return mailnum;
	}
	public void setMailnum(String mailnum) {
		this.mailnum = mailnum;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getDomain() {
		return domain;
	}
	public void setDomain(String domain) {
		this.domain = domain;
	}
	public String getForgetq() {
		return forgetq;
	}
	public void setForgetq(String forgetq) {
		this.forgetq = forgetq;
	}
	public String getForgeta() {
		return forgeta;
	}
	public void setForgeta(String forgeta) {
		this.forgeta = forgeta;
	}
}
